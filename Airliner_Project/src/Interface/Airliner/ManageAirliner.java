    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int row=tblDirectory1.getSelectedRow();
        if(row<0)
        //that means there is an error and user has not selected any row
        {
            JOptionPane.showMessageDialog(null,"Please make a selection");
            return; }
        
        else{
          Airliner a=(Airliner)tblDirectory1.getValueAt(row, 0);
          ViewAirlinerPanel panel=new ViewAirlinerPanel(a,AirlinerPanel);
          CardLayout cardlayout=(CardLayout)AirlinerPanel.getLayout();
          AirlinerPanel.add("ViewPanel",panel);
          cardlayout.next(AirlinerPanel);
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

