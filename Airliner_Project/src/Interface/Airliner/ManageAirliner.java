     public void populate(){
       int rowCount = tblDirectory1.getRowCount();
        DefaultTableModel model = (DefaultTableModel)tblDirectory1.getModel();
        for(int i = rowCount - 1; i >=0; i--) {
            model.removeRow(i);
        }
        
        for(Airliner a : airDir.getAirlinerDirectory()) {
            Object row[] = new Object[3];
            row[0] = a;
            row[1]=a.getCapacity();
       
        
        model.addRow(row);
        
        
        }
                     
        }
        
    
    
    
    
