    private void viewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewActionPerformed
        // TODO add your handling code here:
        int row=tblDirectory.getSelectedRow();
        if(row<0)
        //that means there is an error and user has not selected any row
        {
            JOptionPane.showMessageDialog(null,"Please make a selection");
            return; }
        
        else{
          Product product=(Product)tblDirectory.getValueAt(row, 0);
          ViewPanel panel=new ViewPanel(prodDir,product,ManagePanel);
          CardLayout cardlayout=(CardLayout)ManagePanel.getLayout();
          ManagePanel.add(panel);
          cardlayout.next(ManagePanel);
        }
      
        
        
    }//GEN-LAST:event_viewActionPerformed

