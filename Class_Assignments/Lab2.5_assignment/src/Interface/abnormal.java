    public void populateTable(){
        DefaultTableModel dtm=(DefaultTableModel) tblVitalSign.getModel();
        dtm.setRowCount(0); //this will ensure no rows are there in table when view buttonis clicked and new values are incorporated
    
        for(VitalSign vs: vitalhistory.getAbnormallist(maxBP, minBP)) 
            //arraylist is of type VitalSign so each object of array list will be attribute to the vital sign class and vitalhistory is array
        {
            Object row[]=new Object[2];   //because we want two columns in the table
            row[0]=vs;     
            //this will give us the value of the memory location where vs object is created 
            //also this is how we are binding the object with the table at position 0
            row[1]=vs.getBloodPressure();
            dtm.addRow(row);
        
        }
    
    }
