    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        // TODO add your handling code here:

        int selectedrow=tblVitalSign.getSelectedRow();

        if(selectedrow>=0){
            int dialogRes=JOptionPane.showConfirmDialog(null, "Selected row is deleted from the table");
            if(dialogRes == JOptionPane.YES_OPTION){

                VitalSign vs=(VitalSign)tblVitalSign.getValueAt(selectedrow,0); //to know which object is selected
                vitalhistory.deleteVital(vs);
                //JOptionPane.showMessageDialog(null,"Selected row is deleted from the table");
                populateTable(); //redisplay the table
                resettextfield();
            }

        }
        else
        JOptionPane.showMessageDialog(null,"Select any row from the table");
    }//GEN-LAST:event_DeleteActionPerformed

